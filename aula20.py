var_1 = input('Digite o Primeiro Valor: ')
var_2 = input('Digite o segundo valor: ')

if var_1 > var_2:
    print(f'O primeiro valor {var_1} é maior'
          f' que o segundo valor {var_2}.')

else:
    print(f'O segundo valor {var_2} é maior que o primeiro valor {var_1}', sep ='-') 

